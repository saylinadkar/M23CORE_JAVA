package com.cg.WrapperClass;

public class Autoboxing {

	public static void main(String[] args) {
		float a=65.4f;
		Float b=a;
		Float f=new Float(87.9);// converted primitive datatype into wrapper class
		System.out.println(f);
		System.out.println("a= "+a+"  b="+b+" f="+f);
		
			}

}
